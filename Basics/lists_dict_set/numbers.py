num = 3
floatNum = 3.14

# type of the data of the variables
print(type(num))
print(type(floatNum))

# Not normal operators
print(45 / 43)  # will give output with decimal points wherever needed
print(45 // 43)  # floor value
print(3 ** 3)  # 3 to the power of 3

# round to function
print(round(3.432, 1))  # round to 1 decimal place

